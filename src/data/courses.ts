function buildModules(prefix: string, count = 4): CourseModule[] {
  return Array.from({ length: count }, (_, m) => ({
    id: `${prefix}-m${m + 1}`,
    title: `Module ${m + 1}: ${["Nền tảng", "Kỹ thuật cốt lõi", "Thực hành nâng cao", "Ứng dụng thực tế"][m] ?? "Chuyên đề"}`,
    lessons: Array.from({ length: 4 }, (_, l) => ({
      id: `${prefix}-m${m + 1}-l${l + 1}`,
      title: `Bài ${l + 1}: ${["Giới thiệu", "Khái niệm chính", "Ví dụ thực tế", "Bài tập ứng dụng"][l]}`,
      duration: 8 + l * 4,
      type: (l === 3 ? "quiz" : "video") as "video" | "quiz",
      description:
        "Bài học cung cấp kiến thức nền tảng và ví dụ thực tế giúp học viên áp dụng ngay vào công việc hằng ngày.",
      resources: l === 1 ? [{ name: "Tài liệu bài học.pdf", url: "#" }] : undefined,
    })),
  }));
}

